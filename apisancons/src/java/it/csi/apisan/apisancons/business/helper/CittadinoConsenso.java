package it.csi.apisan.apisancons.business.helper;

public class CittadinoConsenso {
	private String codiceFiscale;
	private String nome;
	private String cognome;
	private String idAura;
	public String getCodiceFisacle() {
		return codiceFiscale;
	}
	public void setCodiceFiscale(String codiceFisacle) {
		this.codiceFiscale = codiceFisacle;
	}
	public String getNome() {
		return nome;
	}
	public void setNome(String nome) {
		this.nome = nome;
	}
	public String getCognome() {
		return cognome;
	}
	public void setCognome(String cognome) {
		this.cognome = cognome;
	}
	public String getIdAura() {
		return idAura;
	}
	public void setIdAura(String idAura) {
		this.idAura = idAura;
	}
	
	
	
}
